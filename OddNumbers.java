package week1.day1.assignments;

public class OddNumbers {

	public static void main(String[] args) {
		//odd numbers
		   
		   int number = 10;
		   System.out.println("Odd numbers :");
		   for (int i = 1; i <= number; i++) 
		   {
				
				if(i % 2 == 1)
				{
					System.out.println(i);
				}
			}

	}

}
